    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     */
    public function search(Request $request){
        
        $number = $request->number ? $request->number : '';
        $type = $request->type ? $request->type : 'local';

        if ($type) {
            $pilot_numbers = PilotNumber::where('number', 'like', '%' . $number . '%')->where('available',1)->where('type',$type)->inRandomOrder()->take(10)->get(); 

            return response()->json($pilot_numbers);
        }

        return collect([]);
    
    }


